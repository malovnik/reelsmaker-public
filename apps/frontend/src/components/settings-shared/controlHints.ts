/**
 * Реестр подсказок всех контролов настроек (Эксперт-студия, спека d3 §2.4).
 *
 * Единственный источник правды для триплета what/effect/advise + honesty-бейдж.
 * Примитивы (`SwitchRow`/`SliderRow`/`SelectRow`/`NumberRow`/`ActionButton`)
 * достают запись по ключу `hintKey: ControlHintKey` — собрать контрол с
 * несуществующим ключом нельзя типобезопасно, поэтому покрытие подсказками —
 * инвариант, а не дисциплина.
 *
 * Формула подсказки (3 части, всегда):
 *   what   — что делает контрол (1 строка, по-русски)
 *   effect — эффект на скорость / качество / стоимость / кадр
 *   advise — рекомендация «оставьте X для Y»
 *   badge  — бейдж честности, если ручка декоративна / опасна / выключена
 */
export interface ControlHint {
  what: string;
  effect: string;
  advise: string;
  badge?: HonestyBadge;
}

