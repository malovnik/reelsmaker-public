#!/usr/bin/env bash
# reelibraLINUX.sh — точка входа Reelibra на Linux (двойной клик / меню / терминал).
#
# ┌─ Как запустить ───────────────────────────────────────────────────────────┐
# │ Вариант 1 (рекомендован): разовая установка ярлыка, дальше запуск из меню. │
# │     bash launchers/linux/install.sh                                        │
# │   После этого Reelibra появится в меню приложений и запускается кликом.     │
# │                                                                            │
# │ Вариант 2: прямой запуск из терминала.                                     │
# │     ./reelibraLINUX.sh                                                      │
# │                                                                            │
# │ Почему не «просто двойной клик по этому файлу»: файл-менеджеры Linux        │
# │ (Nautilus/Dolphin/Nemo) по умолчанию открывают .sh в текстовом редакторе.  │
# │ Установка ярлыка через install.sh — нативный и надёжный путь «двух кликов». │
# └────────────────────────────────────────────────────────────────────────────┘
#
# Требования: x86_64, glibc >= 2.35 (Ubuntu 22.04+/Debian 12+/Fedora 37+), интернет
# при первом запуске. STT на Linux = только Deepgram (нужен DEEPGRAM_API_KEY).

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
exec "$ROOT_DIR/launchers/linux/launcher.sh" "$@"
